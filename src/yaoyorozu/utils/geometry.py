def kabsch(*, target: MOLECULE, ref: MOLECULE) -> MOLECULE: 
    P = np.array(target.coord)
    Q = np.array(ref.coord)

    Pg = np.mean(P, axis=0)
    Qg = np.mean(Q, axis=0)

    P = P - Pg
    Q = Q - Qg

    C = np.dot(P.T,Q)
    V, S, Wt = np.linalg.svd(C)
    d = np.sign(np.linalg.det(np.dot(V, Wt)))
    D = np.diag([1, 1, d]) 
    U = np.dot(np.dot(V, D), Wt) 
    P=P@U

    P = P + Qg
    mole = MOLECULE(atoms=target.atoms,coord=P)

    return mole


def rotate(*, mole: MOLECULE, R: np.ndarray) -> MOLECULE:
    R = np.asarray(R, dtype=float)
    if R.shape != (3, 3):
        raise ValueError(f"R must be 3x3, got {R.shape}")

    coord = mole.coord @ R.T   # (N,3) @ (3,3) -> (N,3)
    return MOLECULE(atoms=mole.atoms, coord=coord)



def measure_geometry(*, mole: MOLECULE, index: index) -> float:
    xyz = mole.coord
    if len(index) == 2:    # bond length
        i, j = index[0], index[1]
        vec = xyz[i] - xyz[j]
        return np.linalg.norm(vec)
        
    elif len(index) == 3:  # angle
        i, j, k = index[0],index[1],index[2]
        vec1 = np.array(xyz[i]) - np.array(xyz[j])
        vec2 = np.array(xyz[k]) - np.array(xyz[j])
        cos_theta = np.dot(vec1, vec2) / (np.linalg.norm(vec1) * np.linalg.norm(vec2))

        cos_theta = np.clip(cos_theta, -1.0, 1.0)
        return np.degrees(np.arccos(cos_theta))
        
    else:
        raise ValueError("Indices must be a tuple of 2 (for bond length) or 3 (for angle).")
